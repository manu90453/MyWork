package com.java.main.objective.ques;

public class SwitchCase2 {
	public static void main(String[] args) {
		int key = 8;
		
		switch (key) {
		case 8:
			System.out.println("ddhdh");
			break;
		case 9:
			System.out.println("sharma1");
			System.out.println("sharma2");
		default:
			System.out.println("manu");
			break;
		}
	}

}
